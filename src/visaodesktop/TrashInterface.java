    public void refreshTrash() {
        String inbox = "";
        messages.setText("");
        ids.removeAllItems();
        for (int i = 0; i < Main.interfaceoff.getServer().getOnline().getTrash().size(); i++) {
            inbox  +=  (i+1 + ") " + Main.interfaceoff.getServer().getOnline().getTrash().get(i).getTitle() + "\n");
            ids.addItem("Mensagem " + (i+1));
        }
        messages.setCaretPosition(0);
        messages.setText(inbox);
    }



