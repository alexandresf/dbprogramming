    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        if (ids.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Sua lixeira está vazia");
        } else {
            if (JOptionPane.showConfirmDialog(null, "Confirmar exclusão da mensagem?\nA mensagem será excluida permanentemente!", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0)
                Main.interfaceoff.getServer().getOnline().deleteMessage(ids.getSelectedIndex());
        }
        refreshTrash();
}//GEN-LAST:event_deleteActionPerformed

    private void readMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_readMouseClicked
        if (ids.getSelectedIndex() == -1)
            JOptionPane.showMessageDialog(this, "Sua lixeira está vazia");
        else {
            message = new MessageInterface(Main.interfaceoff.getServer().getOnline().getTrash().get(ids.getSelectedIndex()));
            Main.interfaceoff.getServer().getOnline().getTrash().get(ids.getSelectedIndex()).setRead();
            message.setVisible(true);
        }
        refreshTrash();
}//GEN-LAST:event_readMouseClicked

